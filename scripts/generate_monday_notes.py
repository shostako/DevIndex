def main():
    input_path = Path("/home/shostako/ClaudeCode/DevIndex/public/terms.json")
    output_path = Path("/home/shostako/ClaudeCode/DevIndex/public/terms_monday.json")

    with open(input_path, 'r', encoding='utf-8') as f:
        data = json.load(f)

    updated_count = 0
    missing_terms = []

    for term_entry in data['terms']:
        term_name = term_entry['term']
        if term_name in MONDAY_NOTES:
            term_entry['sarcastic_notes'] = MONDAY_NOTES[term_name]
            updated_count += 1
        else:
            missing_terms.append(term_name)

    # 更新日時を更新
    from datetime import datetime
    data['updated_at'] = datetime.utcnow().isoformat() + 'Z'

    with open(output_path, 'w', encoding='utf-8') as f:
        json.dump(data, f, ensure_ascii=False, indent=2)

    print(f"Updated: {updated_count} terms")
    print(f"Output: {output_path}")

    if missing_terms:
        print(f"\nMissing terms ({len(missing_terms)}):")
        for t in missing_terms:
            print(f"  - {t}")

if __name__ == "__main__":
    main()
